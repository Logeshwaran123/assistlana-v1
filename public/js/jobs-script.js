// Categories Toggler
const cateJobs = document.querySelectorAll(".category");
for (let index = 0; index < cateJobs.length; index++) {
  cateJobs[index].addEventListener("click", () => {
    cateJobs[index].classList.add("active-cate");
    console.log("Content : " + cateJobs[index].textContent);
    for (let index = 0; index < cateJobs.length; index++) {
      cateJobs[index].classList.remove("active-cate");
    }
    cateJobs[index].classList.add("active-cate");
    let jobs = "";
    if (index == 0) {
      jobs = internships;
    } else if (index == 1) {
      jobs = wfhJobs;
    } else if (index == 2) {
      jobs = dataAnalyst;
    } else if (index == 3) {
      jobs = digitalMarkJobs;
    } else if (index == 4) {
      jobs = designingJobs;
    } else if (index == 5) {
      jobs = softwareTesting;
    }
    let currentCate = "";
    jobs.map((job) => {
      // TEMPLATE
      const template = `<div class="job">
      <div class="job-main">
      <h4 class="job-headline">${job.headline}</h4>
  <div class="important-info">
  <p class="location">Location: ${job.location}</p>
  <p class="date">Vacancies: ${job.nVacancies}</p>
  <p class="date">Experience: ${job.experience}</p>
  <p class="mode">Job Type: ${job.typeOfJob}</p>
  <p class="package">Salary: ${job.salary}</p>
  </div>
  <div class="job-short-description">
  ${job.note}
  <p class="package">${
    job.requirement ? "<strong>Requirements:</strong> " + job.requirement : ""
  }</p>
  </div>
  </div>
  <div class="apply-link">
  <a href="${job.link}" target="_blank" class="apply-link">APPLY LINK</a>
  </div>
  </div>`;
      currentCate += template;
      return template;
    });
    document.querySelector(".jobs-list").innerHTML = currentCate;
  });
}

